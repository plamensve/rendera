let currentLang = 'bg';

function switchLanguage() {
    currentLang = currentLang === 'bg' ? 'en' : 'bg';
    updateLanguage();
}

function updateLanguage() {
    // Update the language buttons text
    document.querySelectorAll('.current-lang').forEach(el => {
        el.textContent = currentLang.toUpperCase();
    });

    // Update all translatable elements
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[currentLang][key]) {
            if (element.tagName.toLowerCase() === 'title') {
                document.title = translations[currentLang][key];
            } else if (element.hasAttribute('alt') || element.hasAttribute('aria-label')) {
                element.setAttribute(
                    element.hasAttribute('alt') ? 'alt' : 'aria-label',
                    translations[currentLang][key]
                );
            } else if (element.hasAttribute('href') && element.getAttribute('href').startsWith('mailto:')) {
                // Keep email addresses unchanged
                element.textContent = element.getAttribute('href').replace('mailto:', '');
            } else if (element.hasAttribute('href') && element.getAttribute('href').startsWith('tel:')) {
                // Keep phone numbers unchanged
                element.textContent = element.getAttribute('href').replace('tel:', '');
            } else {
                element.textContent = translations[currentLang][key];
            }
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = currentLang;

    // Store the language preference
    localStorage.setItem('preferredLanguage', currentLang);
}

// Initialize language from localStorage or browser preference
document.addEventListener('DOMContentLoaded', () => {
    const storedLang = localStorage.getItem('preferredLanguage');
    const browserLang = navigator.language.split('-')[0];

    if (storedLang && ['bg', 'en'].includes(storedLang)) {
        currentLang = storedLang;
    } else if (browserLang === 'en') {
        currentLang = 'en';
    }

    updateLanguage();

    // Add click handlers to language switch buttons
    document.getElementById('langSwitch').addEventListener('click', switchLanguage);
    document.getElementById('mobileLangSwitch').addEventListener('click', switchLanguage);
}); 